"""Постобработка OCR-текста: исправление символов и нормализация кавычек."""
from __future__ import annotations
import re

from .highlight import is_latin_homoglyph_word as _lat_homoglyph_word

# ── Нормализация кавычек ──────────────────────────────────────────────────────
_QUOTE_PATTERNS: list[tuple[re.Pattern, str]] = [
    (re.compile(r'<<\s*([^>\n]{1,300}?)\s*>>'),         r'«\1»'),
    (re.compile(r'[„„]([^\n"]{1,300})["""]'),            r'«\1»'),
    (re.compile(r'"([^"\n]{1,200})"'),                   r'«\1»'),
    (re.compile(r'(?<!\w)"(?=\S)'),                      '«'),
    (re.compile(r'(?<=\S)"(?!\w)'),                      '»'),
]

# ── Исправление символов ──────────────────────────────────────────────────────
def _nl_outside_parens(m: re.Match) -> str:
    """Перенос реквизита на новую строку, но ТОЛЬКО если он не внутри «(…)».
    Внутри открытой скобки (реквизиты в теле: «(… ОГРН:… ИНН:…)») — оставляем
    инлайн, иначе justified-абзац рвётся."""
    before = m.string[:m.start()]
    if before.count("(") > before.count(")"):
        return m.group(0)                 # внутри скобок — без переноса
    return "\n" + m.group(1)


_OCR_CHAR_FIXES: list[tuple[re.Pattern, object]] = [
    # ── Цифры ────────────────────────────────────────────────────────────────
    # ] читается как 1 в числовых контекстах (рядом с цифрой или в начале строки)
    (re.compile(r'(?<=\d)\]'),                                                '1'),
    (re.compile(r'\](?=\d)'),                                                 '1'),
    # ] в начале строки/слова как нумерация («]. текст» → «1. текст»)
    (re.compile(r'(?<!\w)\]\s*[.)]\s+(?=[А-ЯЁA-Za-z\d])'),                  '1. '),
    # + между цифрами — OCR-замена цифры разделительным символом (3+4000 → 344000)
    (re.compile(r'(?<=\d)\+(?=\d{3}\b)'),                                    ''),
    (re.compile(r'\b(\d)\+(\d)(?=\d)'),                                      r'\1\2'),
    # ── Нумерованные списки: I. или l. в начале строки/токена ─────────────────
    # OCR часто читает цифру «1» как латинскую «I» или «l» в нумерации.
    # Безопасно менять только в начале строки/блока перед точкой/скобкой.
    # «I. Название» / «l. Название» → «1. Название»
    (re.compile(r'(?<!\w)[Il]\.\s+(?=[А-ЯЁ])'),                              '1. '),
    # «I) » / «l) » в начале строки перед текстом → «1) »
    (re.compile(r'(?<!\w)[Il]\)\s+(?=[А-ЯЁ])'),                              '1) '),

    # ── Буква B (латинская) в кириллическом тексте = В ───────────────────────
    # В верхнем регистре B и В идентичны визуально; OCR путает их
    (re.compile(r'(?<=[А-ЯЁ])\s+B\b'),                                       ' В'),
    (re.compile(r'(?<=[а-яё])\s+B\b'),                                       ' в'),
    (re.compile(r'\bB\s+(?=[А-ЯЁ])'),                                        'В '),
    (re.compile(r'\bB\s+(?=[а-яё])'),                                        'в '),

    # ── Пунктуация ───────────────────────────────────────────────────────────
    # Двойная пунктуация ,; → , (ИНН 6163011391,; ОГРН → ИНН 6163011391, ОГРН)
    (re.compile(r',\s*;'),                                                    ','),
    # ; → , перед адресными сокращениями (пр., ул., пер., д., стр., корп.)
    # ПЕРВЫЙ проход: работает когда аббревиатура уже в форме пр./ул. и т.д.
    (re.compile(r';\s*(?=(?:пр|ул|пер|пл|д|стр|корп|кв)\s*[.\s])'),         ', '),
    # ; → , после р-н (Мясниковский р-н; х → Мясниковский р-н, х)
    (re.compile(r'(?<=р-н)\s*;'),                                             ','),

    # ── Знак № ───────────────────────────────────────────────────────────────
    (re.compile(r'\bN[o°о]\.?\s*(?=\d)'),                                    '№ '),
    (re.compile(r'\bNg\.?\s*(?=\d)'),                                        '№ '),
    (re.compile(r'\bN[go°о]\.?\s+(?=[А-ЯЁA-Z]\d)'),                         '№ '),
    # N' / N" перед буквой или цифрой → № (апостроф/кавычка = артефакт OCR)
    (re.compile(r"\bN['\"](?=\s*[A-ZА-Яа-я\d])"),                           '№ '),
    # «Ngl» → «№ 1» (l — это «1»); «Ng1»/«Ng2» → «№ 1»/«№ 2» (слитно с цифрой)
    (re.compile(r'\bNgl\b'),                                                  '№ 1'),
    (re.compile(r'\bNg(\d)'),                                                r'№ \1'),
    # «Ng» как отдельный токен (за ним код из букв: «Ng SRSRS50…») → «№».
    # В русском «Ng» не встречается как слово, поэтому замена изолированного токена безопасна.
    (re.compile(r'\bNg\b'),                                                   '№'),
    # Одиночная латинская N перед числом = № («…банкротстве N 127-ФЗ» → «№ 127-ФЗ»)
    (re.compile(r'(?<![A-Za-z])N\s+(?=\d{2,})'),                             '№ '),

    # ── Аббревиатуры ─────────────────────────────────────────────────────────
    # К/с (корреспондентский счёт): латинская K вместо К
    (re.compile(r'\bK[l/][сc]\b'),                                           'К/с'),

    # ── г. (город) ───────────────────────────────────────────────────────────
    # & или &. перед словом с заглавной буквы; убрали \b — он не ловил & после ; или ,
    # [А-ЯЁA-Z] — латинская P часто путается с кириллической Р (напр. «Pocmов»)
    (re.compile(r'(?<!\w)&\.?\s+(?=[А-ЯЁA-Z])'),                            'г. '),

    # ── пр. (проспект) ───────────────────────────────────────────────────────
    # [рp] — OCR часто даёт латинскую p вместо кириллической р («иp:» вместо «ир:»)
    (re.compile(r'\bи[рp][;:]\s*(?=[А-ЯЁа-яёA-Za-z])'),                    'пр. '),
    (re.compile(r'\bmp[:.]\s*'),                                             'пр. '),
    # ВТОРОЙ проход `;` → `,`: после фикса иp: → пр. теперь «; пр.» виден явно
    (re.compile(r';\s*(пр|ул|пер|пл|д|стр|корп|кв)\.'),                    r', \1.'),

    # ── Кавычки «» ───────────────────────────────────────────────────────────
    # ВАЖНО: сначала двойные скобки, потом одиночные — иначе <<text>> даёт <«text»>
    # <<text>> или << text >>
    (re.compile(r'<<\s*([А-ЯЁа-яёA-Za-z][^>\n]{1,200}?)\s*>>'),             r'«\1»'),
    # <text> — одиночные угловые скобки
    (re.compile(r'(?<!\w)<([А-ЯЁа-яёA-Za-z][^<>\n]{1,200}?)>(?!\w)'),       r'«\1»'),
    # <text» — открывающая < и правильная »
    (re.compile(r'(?<!\w)<([А-ЯЁа-яёA-Za-z][^<\n»]{1,100}?)»'),             r'«\1»'),
    # <text э/ж — закрывающая » читается как э или ж
    (re.compile(r'(?<!\w)<([А-ЯЁа-яёA-Za-z][^<\n]{1,100}?)[эж](?=\W|$)'),  r'«\1»'),
    # @text — открывающая « читается как @
    (re.compile(r'(?<!\w)@([А-ЯЁа-яёA-Za-z][^@\n]{1,100}?)[эж](?=\W|$)'),  r'«\1»'),
    (re.compile(r'(?<!\w)@([А-ЯЁа-яёA-Za-z][^@\n»>]{1,100}?)»'),            r'«\1»'),
    # @text> — открывающая @ и закрывающая >
    (re.compile(r'(?<!\w)@([А-ЯЁа-яёA-Za-z][^@\n>]{1,100}?)>(?!\w)'),       r'«\1»'),

    # ── ПСБ банк: «БАНК ПСБ» — восстановление кавычек ───────────────────────
    # OCR читает открывающую «  как ~, Ж, ж, К, к, Ф, :, " прямо перед БАНК ПСБ.
    # Закрывающая » → ж/х/э или теряется. Якорь: слитно с БАНК (без пробела).
    (re.compile(r'[~ЖжКкФф:"<(]([Бб][Аа][Нн][Кк])\s+ПСБ[»"жхэ]?'),       r'«\1 ПСБ»'),

    # ── ПСБ: CTU → ст. (расширение существующего CT-правила) ─────────────────
    # OCR читает «ст.» (с точкой) как CTU (C=с, T=т, U=артефакт точки/суффикс)
    (re.compile(r'\bCTU\s*\.?\s*(?=\d)'),                                   'ст. '),

    # ── ПСБ: цифры с буквами в датах (I→1, l→1, U→0, Z→2) ──────────────────
    # Пример: IU.Il.2UZ2 → 10.11.2022; Z6.10.ZUU2 → 26.10.2002
    # Применяем только если в паттерне есть хотя бы одна OCR-буква.
    (re.compile(r'\b(?=[\dIlZU]{1,2}\.[\dIlZU]{1,2}\.[\dIlZU]{4}\b)[\dIlZU]{1,2}\.[\dIlZU]{1,2}\.[\dIlZU]{4}\b'),
        lambda m: re.sub(r'[Il]', '1', re.sub(r'U', '0', re.sub(r'Z', '2', m.group(0))))
        if re.search(r'[IlZU]', m.group(0)) else m.group(0)),
    # Денежные суммы: «448,U0» → «448,00» (U = 0 между цифрами в дробной части)
    (re.compile(r'(\d+[,.])\s*U(\d)'),                                      r'\g<1>0\2'),

    # ── Батлер / ФНС: и → нн (EasyOCR: буква и читается как нн) ─────────────
    # Буква «и» в словах с плохим пропечатыванием читается как двойное «нн».
    # Специфично для конкретных слов — безопасно расширяем только их корни.
    (re.compile(r'\bдолжнн([кгмч]\w*)'),                                    r'должни\1'),
    (re.compile(r'\bДолжнн([кгмч]\w*)'),                                    r'Должни\1'),
    (re.compile(r'\bгражданнна\b'),                                         'гражданина'),
    (re.compile(r'\bГражданнна\b'),                                         'Гражданина'),
    (re.compile(r'\bгражданнн([аоуеий]\w*)'),                               r'гражданин\1'),
    (re.compile(r'\bОТНЕШеНН\b'),                                           'отношении'),
    (re.compile(r'\bВКЛЮЧеНИН\b'),                                          'включении'),

    # ── Батлер / ФНС: кириллица о ↔ латинская u в словах ─────────────────────
    # EasyOCR путает кирилл. «о» с латинским «u» в некоторых шрифтах сканов.
    # Пример: «сuuтветствии» → «соответствии», «Верховногu» → «Верховного».
    # Обрабатываем СЕРИЮ u+ между кириллическими буквами (сuu→соо, не только u→о).
    (re.compile(r'(?<=[А-Яа-яЁё])u+(?=[А-Яа-яЁё])'),
        lambda m: 'о' * len(m.group(0))),
    # u в конце слова после кириллицы (Верховногu, Итогu → Верховного, Итого)
    (re.compile(r'(?<=[А-Яа-яЁё]{2})u(?=[\s.,;:()\-»"!\?]|$)'),            'о'),

    # ── Банковские реквизиты ─────────────────────────────────────────────────
    # «Plc:» / «P/c:» / «Р/c:» → «Р/с:» (расчётный счёт, OCR путает буквы)
    (re.compile(r'\bPl[cс]\s*:'),                                             'Р/с:'),
    (re.compile(r'\b[PR]/[cс]\s*:'),                                          'Р/с:'),
    # «Корасч» / «Корасч:» → «Кор/сч.:»
    (re.compile(r'\bКорасч\s*[.:]?\s*', re.IGNORECASE),                      'Кор/сч.: '),
    # «Получатель: АO» в конце блока — OCR оборвал перед «АЛЬФА-БАНК»
    # Оставляем — слияние делается через coplanar или continuation
    # «АO» → «АО» (латинская O вместо кириллической О)
    (re.compile(r'\bАO\b'),                                                    'АО'),

    # ── Кавычки в названиях организаций: «(» прочитана вместо ««» ─────────────
    # OCR стабильно читает открывающую кавычку « как «(», а иногда теряет её.
    # После орг.-правовой формы (АО/ПАО/ООО/ОАО/ЗАО) или слова ОБЩЕСТВО название
    # всегда в кавычках «…». Закрывающая » добавляется/нормализуется.
    # ВАЖНО: срабатывает только если за формой идёт ЗАГЛАВНОЕ слово — реальные
    # парентезы со строчной буквы («(далее …)») не затрагиваются.
    # Закрывающая кавычка может быть ещё не нормализована (прямая " вместо »),
    # т.к. эти правила идут ДО fix_quotes — поэтому принимаем ["»].
    # Открывающая кавычка OCR: ( < @ (не-буквенные гомоглифы — безопасно после
    # орг.-правовой формы). Имя: с заглавной, mixed-case, до двух слов
    # («Айди Коллект», «Экспобанк», «АЛЬФА-БАНК»). Закрывающая: » или прямая ".
    # Якорь «форма + ( < @» исключает реальные парентезы «(далее …)», «(А53)».
    (re.compile(r'\bОБЩЕСТВО\s*[(<@]([А-ЯЁ][а-яёА-ЯЁ\-]+(?:\s+[А-ЯЁ][а-яёА-ЯЁ\-]+)?)["»]?'),
                                                                              r'ОБЩЕСТВО «\1»'),
    (re.compile(r'\b(АО|ПАО|ООО|ОАО|ЗАО|ПКО|МФК)\s*[(<@]([А-ЯЁ][а-яёА-ЯЁ\-]+(?:\s+[А-ЯЁ][а-яёА-ЯЁ\-]+)?)["»]?'),
                                                                              r'\1 «\2»'),
    (re.compile(r'\b(АО|ПАО|ООО|ОАО|ЗАО)\s+([А-ЯЁ][А-ЯЁ\-]+)["»]'),          r'\1 «\2»'),
    # «МФК ФБыстроденьгих» / «МФК кбыстроденьгих» / «МФК «Быстроденьгих»» →
    # «МФК «Быстроденьги»». OCR читает « как Ф/к/( и теряет »→х/ж/э на конце.
    # Якорь «МФК + стем быстроденьг» — конкретный кредитор, ложных нет.
    (re.compile(r'\b(МФК)\s+[Фк«(<]?\s*[Бб]ыстроденьг[а-яё]*[»"эжх]?'),       r'\1 «Быстроденьги»'),

    # ── Центр-инвест: OCR иногда теряет первую Ц ─────────────────────────────
    (re.compile(r'«[еЕ]нтр-[иИ]нвест»'),                                    '«Центр-инвест»'),
    # «Текст»: в конце блока → «Текст». (OCR ставит : вместо . после »)
    (re.compile(r'»\s*:(\s*$)'),                                            r'».\1'),

    # ── centrinvest.ru ───────────────────────────────────────────────────────
    # Покрываем варианты OCR: пи н; / пu n; / mu n; / ru n; / pu n;
    (re.compile(r'(centrinvest)\s+(?:пи|пu|mu|ru|pu)\s*[нn]?[;,.]?',
                re.IGNORECASE),                                               r'\1.ru'),
    (re.compile(r'(@centrinvest)\s+(?:ru|пu|pu)\b', re.IGNORECASE),         r'\1.ru'),

    # ── ПРОШУ / ЗАЯВЛЕНИЕ — OCR артефакты в заголовках ──────────────────────
    # «П ₽ 0 ШУ:» → «ПРОШУ:»  (OCR разбивает ПРОШУ на символы)
    (re.compile(r'П\s*[₽P]\s*0\s*ШУ\s*:?', re.IGNORECASE),                  'ПРОШУ:'),
    # «включснии» / «включсния» → «включении» (с→е ошибка OCR)
    (re.compile(r'включснии', re.IGNORECASE),                                 'включении'),
    (re.compile(r'трсбований', re.IGNORECASE),                                'требований'),
    (re.compile(r'крсдиторов', re.IGNORECASE),                                'кредиторов'),
    (re.compile(r'трсбовани', re.IGNORECASE),                                 'требовани'),
    # «SRSRS5 ] 0S23» → «SRSRS510S23» (] и пробелы внутри кода соглашения)
    # Паттерн: буквы/цифры + пробел + ] + пробел + цифры → слитно
    (re.compile(r'([A-Z0-9]+)\s+\]\s+([0-9][A-Z0-9]*)'),                     r'\1\2'),
    # «Jama» → «Дата» (J→Д, a→а, m→т — OCR латиница вместо кириллицы курсива)
    (re.compile(r'\bJama\b'),                                                  'Дата'),
    # «peeucmpauuu» → «регистрации» (полная замена этого OCR-артефакта)
    (re.compile(r'peeucmpauuu', re.IGNORECASE),                               'регистрации'),
    # «Mecmo» → «Место» (Me→Ме, c→с, m→т, o→о — латиница в кириллическом слове)
    (re.compile(r'\bMecmo\b'),                                                 'Место'),
    # «нахоэкоения» → «нахождения»
    (re.compile(r'нахо[эж]коения', re.IGNORECASE),                            'нахождения'),
    # «юриоического» / «юриоическоео» → «юридического»
    (re.compile(r'юри[оo]ическ[оo][еe][оo]', re.IGNORECASE),                 'юридического'),
    (re.compile(r'юри[оo]ическ[оo]\b', re.IGNORECASE),                       'юридического'),
    # «лuца» / «лоца» → «лица»
    # «лuца»: u латинская вместо и (ловим ДО общего u→о).
    # «лоца»: общий u→о превратил «лuца» → «лоца» — ловим после него.
    (re.compile(r'л[uиоу]ца\b'),                                              'лица'),
    # «Москеа» → «Москва» (е вместо в + а)
    (re.compile(r'\bМоскеа\b'),                                               'Москва'),
    # «Каланчееская» → «Каланчёвская»
    (re.compile(r'Каланчееская', re.IGNORECASE),                              'Каланчёвская'),
    # «2. Mecmo» в начале или середине — нумерация слилась с текстом
    (re.compile(r'\b2\.\s+(?=Место\s+нахожд)', re.IGNORECASE),               ''),
    # «, 2 Москва» → «, г. Москва» (цифра 2 вместо г.)
    (re.compile(r',\s*2\s+Москва\b'),                                         ', г. Москва'),
    # «ул: Каланчёвская» → «ул. Каланчёвская»; «д: 27» → «д. 27» (двоеточие вместо точки)
    (re.compile(r'\bул\s*:\s*(?=[А-ЯЁа-яё])'),                               'ул. '),
    (re.compile(r';\s*д\s*[:.]\s*(\d)'),                                      r', д. \1'),
    # «; 0.27» / «; 0,27» → «; д. 27» (OCR: д читается как 0, точка как разделитель)
    (re.compile(r';\s*0[.,](\d{1,3})\b'),                                     r'; д. \1'),
    # Реквизиты юрлица в ШАПКЕ: разбиваем длинный блок на строки (каждый реквизит
    # с новой строки). НО только ВНЕ скобок — реквизиты внутри «(…)» в теле
    # («Между ООО МФК (Почтовый адрес:… ОГРН:… ИНН:… КПП:…)») должны идти инлайн,
    # иначе абзац рвётся. Перенос делаем только если скобка не открыта (см. _nl_outside_parens).
    (re.compile(r'\s+(ИНН\s*:)'),                                              _nl_outside_parens),
    (re.compile(r'\s+(ОГРН\s*:)'),                                             _nl_outside_parens),
    (re.compile(r'\s+(Дата\s+регистрации\s*:)', re.IGNORECASE),               _nl_outside_parens),
    (re.compile(r'\s+(Место\s+нахождения)', re.IGNORECASE),                   _nl_outside_parens),
    # «(Назначение платсжа>» / «<Назначение платежах» → ««Назначение платежа»»
    # OCR: открывающая кавычка → ( или <, закрывающая » → ), >, х, ж, э.
    (re.compile(r'[<(«]Назначение\s+плат[сeе]жа[)>хжэ»]', re.IGNORECASE),    '«Назначение платежа»'),
    # «платсжном» → «платежном»; «платсжа» → «платежа»
    (re.compile(r'плат[сe]жн', re.IGNORECASE),                               'платежн'),
    (re.compile(r'плат[сe]жа', re.IGNORECASE),                               'платежа'),
    # «<Получатель» → «"Получатель»
    (re.compile(r'[<(«]Получатель\b'),                                        '"Получатель'),
    # «ИНН по согласованию финансовым При» → «ИНН должника) по согласованию финансовым»
    # Патерн: «ИНН по согласованию финансовым При» в конце блока
    (re.compile(r'ИНН\s+по\s+согласованию\s+финансовым\s+При', re.IGNORECASE),
                                                                              'ИНН должника) по согласованию финансовым'),
    # «управляющимэ» / «управляющимж» / «управляющимх» → «управляющим»  (э/ж/х = OCR закрывающей »)
    (re.compile(r'управляющим[эжх]\b', re.IGNORECASE),                       'управляющим»'),
    # «должника) управляющим» (второй «должника)» — OCR дублирует) → «управляющим»
    # Паттерн: «финансовым должника) управляющим[»"]» → «финансовым управляющим[»"]»
    (re.compile(r'финансовым\s+должника\)\s+управляющим([»"эж]?)', re.IGNORECASE),
                                                                              r'финансовым управляющим\1'),
    # «по согласованию финансовым управляющим» → «по согласованию с финансовым управляющим»
    (re.compile(r'по\s+согласованию\s+финансовым\s+управляющим', re.IGNORECASE),
                                                                              'по согласованию с финансовым управляющим'),

    # ── Разные ───────────────────────────────────────────────────────────────
    # «0 включении» → «о включении» (ноль вместо буквы о)
    (re.compile(r'\b0\s+включени'),                                          'о включени'),
    # «0 <предложное сущ.>» → «о …» (ноль = предлог «о»). Якорь по словам, чтобы
    # не задеть числовые «0 рублей/копеек»: «0 физических лицах», «0 признании» и т.п.
    (re.compile(r'\b0\s+(?=(?:физическ|юридическ|признани|несостоят|защите|'
                r'внесении|потребительск|микрофинансов|введени|банкротстве|'
                r'состоявш|целях|том\b))', re.IGNORECASE),                   'о '),
    # «соответствин» → «соответствии» (OCR: финальное и→н). В юр. текстах
    # формула «в соответствии с …» устойчива; род. мн. «соответствий» почти не
    # встречается — поэтому замена безопасна (спелл-фиксер её не берёт: неоднозначно).
    (re.compile(r'\bсоответствин\b', re.IGNORECASE),                         'соответствии'),
    # «выеизложенного» → «вышеизложенного» (OCR: «ше» схлопнулось в «е»)
    (re.compile(r'\bвыеизложенного\b', re.IGNORECASE),                       'вышеизложенного'),
    # «соответствии» в начале блока — Docling потерял предлог «в соответствии с»
    # Паттерн надёжен: «соответствии» с маленькой не бывает в начале предложения
    # без предлога «в». Lookahead ловит как п./п: так и ст./ст:
    (re.compile(r'^[Сс]оответствии\s+(?=п[.,:\s]|ст[.,:\s]|П[.,:\s])'),     'в соответствии с '),
    # «в соответствии Приложением» → «в соответствии с Приложением»
    # OCR теряет предлог «с»; «в соответствии» всегда требует «с» в рус. юридическом
    (re.compile(r'\bв\s+соответствии\s+(?=[А-ЯЁ])'),                        'в соответствии с '),
    # «вышеизложенного соответствии» → «вышеизложенного, в соответствии»
    # OCR теряет «в» после «вышеизложенного»; устойчивая правовая формула
    (re.compile(r'\bвышеизложенного\s+соответствии\b'),                     'вышеизложенного, в соответствии'),
    # Инициалы: «Л:ВХалиль» → «Л.В.Халиль»  (слитные инициалы с OCR-двоеточием)
    (re.compile(r'([А-ЯЁа-яёA-Za-z]):([А-ЯЁA-Z])([А-ЯЁA-Z][а-яёa-z]+)'),  r'\1.\2.\3'),
    # «Госпошлина :» → «Госпошлина:»
    (re.compile(r'(Госпошлина)\s+:'),                                        r'\1:'),
    # «руб:» → «руб.»
    (re.compile(r'\bруб\s*:(\s|$)'),                                         r'руб.\1'),
    # | → / в аббревиатурах: р/с, к/с, ИНН/КПП
    (re.compile(r'\b([а-яёА-ЯЁa-zA-Z])\s*\|\s*([а-яёА-ЯЁa-zA-Z])\b'),     r'\1/\2'),
    # \ → / между буквами/цифрами
    (re.compile(r'(\w)\\(\w)'),                                              r'\1/\2'),
    # Email: пробелы вокруг @
    (re.compile(r'(\w)\s+@\s*(\w)'),                                         r'\1@\2'),
    (re.compile(r'(\w)\s*@\s+(\w)'),                                         r'\1@\2'),
    # URL/email: пробел перед доменом верхнего уровня
    (re.compile(r'(\w)\s+\.(ru|рф|com|net|org|info|pro)\b', re.IGNORECASE), r'\1.\2'),
    # email: домен без точки перед TLD, заякорено на @ («@alfabank ru» → «@alfabank.ru»)
    (re.compile(r'(@[\w.\-]+)\s+(ru|рф|com|net|org|info|pro)\b',
                re.IGNORECASE),                                              r'\1.\2'),
    # email: пробел в локальной части перед @ (латиница, оба токена ≥3 букв):
    # «dilyara zainullina@» → «dilyara.zainullina@». Якорь @ + длина снижают ложные.
    (re.compile(r'\b([a-z]{3,})\s+([a-z]{3,}@)', re.IGNORECASE),            r'\1.\2'),
    # http:// с пробелами
    (re.compile(r'https?\s*:\s*/\s*/\s*', re.IGNORECASE),                   r'http://'),
    # www с искажённой точкой
    (re.compile(r'\bwww\s*[.,]\s*(\w)', re.IGNORECASE),                     r'www.\1'),
    # OCR читает линию подчёркивания абзаца как символы «_»: «прав_размер» →
    # «прав размер», «3264_руб__сумма» → «3264 руб сумма». Заменяем на пробел
    # ТОЛЬКО рядом с кириллицей — латинский «_» (email UK_DVPZ@…) не трогаем.
    (re.compile(r'(?<=[А-Яа-яЁё])_+|_+(?=[А-Яа-яЁё])'),                      ' '),
    # Двойной пробел → одинарный
    (re.compile(r'  +'),                                                     ' '),
    # Дефисный перенос: «сло- во» → «слово»
    (re.compile(r'([а-яёА-ЯЁ])-\s+([а-яёА-ЯЁ])'),                          r'\1\2'),

    # ── Пунктуация внутри слов и сокращений ──────────────────────────────────
    # «Ne2» / «Ne 2» → «№ 2»  (латинская e вместо кириллической, перед цифрой)
    (re.compile(r'\bNe\.?\s*(\d)'),                                          r'№ \1'),
    # «документ Ne» / «договор Ne» — «Ne» как отдельный токен после кириллицы → №
    # Безопасно: в русском «Ne» никогда не является словом; перед заглавной или цифрой.
    (re.compile(r'(?<=[А-Яа-яЁё])\s+Ne\b(?!\s*\d)'),                       ' №'),
    # СЛИТНЫЙ «Ne»/«Nе» сразу после кириллицы (OCR потерял пробел и №):
    # «требованияNе 14-02» → «требования № 14-02». «N[еe]» внутри рус. слова не
    # бывает, поэтому замена безопасна. e — латинская или кириллическая.
    (re.compile(r'(?<=[А-Яа-яёЁ])N[еe](?=\s|\d)'),                          ' №'),
    # «соответствии П:» → «соответствии п.»  (заглавная П в аббревиатуре)
    (re.compile(r'(?<=\s)П:\s*(?=[а-яёА-ЯЁ\d])'),                          'п. '),
    # «2019г ;» → «2019г.»  (пробел + точка с запятой после года)
    (re.compile(r'(\d{4}г)\s+;'),                                            r'\1.'),
    # ── Концевое «:» вместо «.» (OCR) — иначе абзацы ложно склеиваются ────────
    # «…14.10.2025 г:» → «…14.10.2025 г.»  (год + г + двоеточие в КОНЦЕ блока)
    (re.compile(r'(\d{4}\s+г)\s*:\s*$'),                                     r'\1.'),
    # «…исполнительное производство:» → «…производство.» (в конце блока)
    (re.compile(r'(производство)\s*:\s*$', re.IGNORECASE),                   r'\1.'),
    # «…ст.438 ГК РФ:» → «…ГК РФ.» (в конце блока)
    (re.compile(r'(ГК\s+РФ)\s*:\s*$'),                                       r'\1.'),
    # «Включить реестр» → «Включить В реестр»  (пропущен предлог «в»)
    (re.compile(r'\bВключить\s+реестр\b'),                                   'Включить в реестр'),
    # «ресстр» → «реестр» (OCR: ее→ес)
    (re.compile(r'\bресстр(?=\w*\s+требований)', re.IGNORECASE),             'реестр'),
    # «… реестр требований кредиторов» без предлога «в» → «… в реестр требований…»
    # (?<!в ) — не дублируем, если предлог уже есть.
    (re.compile(r'(?<!в )\bреестр\s+требований\s+кредиторов', re.IGNORECASE),
                                                                              'в реестр требований кредиторов'),
    # «7 017,ОЗруб:» → «7 017,03 руб.» (кир. ОЗ→03, склейка с «руб», двоеточие→точка)
    (re.compile(r'(\d)[,.]\s*ОЗ\s*руб\s*[:.]?'),                            r'\1,03 руб.'),
    # «перечислении денежных» в начале блока — OCR потерял «При»
    (re.compile(r'^перечислении\s+(ден[её]жных|денсжных|средств)', re.IGNORECASE), r'При перечислении \1'),
    # «денсжных» → «денежных» (OCR путает е→с)
    (re.compile(r'ден[сc]жных', re.IGNORECASE),                              'денежных'),
    # «Заемшиком»/«заемщик»/«Заёмшика» → «Заёмщик…» (OCR: ё→е, щ→ш). Частый
    # термин кредитных договоров; регистр первой буквы и окончание сохраняем.
    (re.compile(r'\b([Зз])а[еёe]м[шщ]ик(\w*)'),                            r'\1аёмщик\2'),
    # «возврата кредита не позднее» в начале блока — OCR пропустил «Срок»
    (re.compile(r'^возврата\s+(кредита\s+не\s+позднее)', re.IGNORECASE),     r'Срок возврата \1'),

    # ── Ростов-на-Дону: OCR даёт латиницу вместо кириллицы ─────────────────────
    # «Pocmов» (P=Р, o=о, c=с, m=н латинские) → «Ростов»
    # Покрываем любую комбинацию кириллических/латинских Р/P, о/o, с/c, н→m/т
    (re.compile(r'[РP][оo][сc][mтtТM][оo][вB]'), 'Ростов'),
    (re.compile(r'[РP][оo][сc][mтtТM][оo]вск'),  'Ростовск'),

    # ── Юридические документы (арбитраж) ─────────────────────────────────────
    # «г:» в конце даты → «г.»  (26.05.2025г: → 26.05.2025г.)
    (re.compile(r'(\d{4})г\s*:'),                                            r'\1г.'),
    # Хвостовое одиночное «Г» после полной даты — OCR-мусор:
    # «28.03.2001 Г место рождения» → «28.03.2001 место рождения»
    (re.compile(r'(\d{2}\.\d{2}\.\d{4})\s+Г\s+(?=[А-Яа-яЁё])'),              r'\1 '),
    # Двоеточие вместо точки после ст/п/пп/ч → «ст:» → «ст.»
    (re.compile(r'\b(ст|п|пп|ч|CT|cт)\s*:\s*(?=\d)'),                      r'\1. '),
    # Латинская CT → ст (артикул ГК/АПК/ФЗ: «CT: 63» → «ст. 63»)
    (re.compile(r'\bCT\s*[:.]\s*(?=\d)'),                                   'ст. '),
    # «Хa основании» / «Hа основании» → «На основании» (X/Х/H вместо Н)
    (re.compile(r'(?<!\w)[ХXH][аa]\s+основани'),                            'На основани'),
    # «акционернос» → «акционерное» (е→с на конце в OCR)
    (re.compile(r'акционернос\b'),                                           'акционерное'),
    # «коммерчсский» → «коммерческий» (пропущена «е»)
    (re.compile(r'коммерчсский\b'),                                          'коммерческий'),
    # Номер дела без дефиса: «А533675» → «А53-3675» (6+ цифр после буквы)
    # Паттерн: буква + 2 цифры + 4+ цифр → буква + 2 цифры + дефис + 4+ цифр
    (re.compile(r'\b([А-ЯЁA-Z])(\d{2})(\d{4,})/'),                         r'\1\2-\3/'),
    # Площадь: «1 17,4» → «117,4» (пробел внутри числа перед запятой)
    # Осторожно: только если первая цифра одна и следом идут 2+ цифры
    (re.compile(r'(?<!\d)(\d)\s+(\d{2,}[,.]\d)'),                          r'\1\2'),
    # «33 коп ; руб.» → «руб. 33 коп.» (перепутан порядок суммы)
    (re.compile(r'(\d+)\s+коп\s*[;,]?\s*руб\.?'),                          r'руб. \1 коп.'),

    # ── Денежные суммы: OCR выносит «руб.»/«рублей» в конец после скобки ──────
    # В многоколоночной вёрстке «руб.» и «рублей» стоят в правой колонке и
    # читаются ПОСЛЕ парентезы прописью, отрываясь в конец строки.
    # «копсск»/«копеск» → «копеек» (OCR: ее→сс)
    (re.compile(r'\bкоп[се]ск\b', re.IGNORECASE),                            'копеек'),
    # A) «0,00 (Ноль 00 копеек); руб. рубл*» → «0,00 руб. (Ноль рубл* 00 копеек);»
    # Сумма прописью без слова «рубл*»; «руб. рубл*» оторваны в конец. Слово
    # прописью «рубл\w+» (рублей/рубля/рубль) КАПТУРИМ (\5) и вставляем в скобку —
    # форма (ед./мн.ч.) берётся из исходного OCR. Негативный lookahead (?!рубл) не
    # даёт захватить уже имеющееся «рубл*». Группа 4 — хвост («; в том числе:»,
    # «; а именно:») между скобкой и оторванными «руб. рубл*».
    (re.compile(
        r'(\d[\d\s]*,\d{2})\s*\(\s*((?:(?!рубл)[А-ЯЁа-яё\s])+?)\s+(\d{1,2})\s+коп\w*\s*\)'
        r'\s*([;:][^()\n]*?)?\s*руб\.?\s*(рубл\w+)',
        re.IGNORECASE),
        r'\1 руб. (\2 \5 \3 копеек)\4'),
    # B) «0,00 руб. (Ноль 00 копеек) а именно: рубл*» → «0,00 руб. (Ноль рубл* 00 копеек) а именно:»
    # «руб.» уже стоит перед скобкой, но слово прописью «рубл*» оторвано в конец.
    # Хвост (\4) — любой текст между скобкой и «рубл*» («а именно:», «; в том числе:»
    # или пусто); (?!рубл) не даёт хвосту съесть само слово прописью.
    (re.compile(
        r'(\d[\d\s]*,\d{2})\s*руб\.\s*\(\s*((?:(?!рубл)[А-ЯЁа-яё\s])+?)\s+(\d{1,2})\s+коп\w*\s*\)'
        r'((?:(?!рубл)[^()\n])*?)\s*(рубл\w+)\b',
        re.IGNORECASE),
        r'\1 руб. (\2 \5 \3 копеек)\4'),
    # C) «0,00 (Ноль рублей 00 копеек); руб.» → «0,00 руб. (Ноль рублей 00 копеек);»
    # «руб.» оторван в конец после скобки (парентеза уже содержит «рубл*»).
    (re.compile(
        r'(\d[\d\s]*,\d{2})\s*(\([^)]*?коп\w*\s*\))\s*([;:])?\s*руб\.?(?!\w)',
        re.IGNORECASE),
        r'\1 руб. \2\3'),
    # «<0»/«<06» = открывающая кавычка «О»/«Об» (OCR: «→<, О→0/О/о, б→6).
    # «<0 потребительском» → ««О потребительском»; «<06 электроэнергетике» → ««Об …»;
    # «<о несостоятельности» → ««О несостоятельности». <06 раньше <0.
    # [0Оо] — ноль ИЛИ кириллическая О/о.
    (re.compile(r'<[0О]6(?=\s)'),                                          '«Об'),
    (re.compile(r'<[0Оо](?=\s)'),                                          '«О'),
    # Закрывающая » для частого закона: ««О несостоятельности (банкротстве)» без »
    # → добавить » (OCR теряет закрывающую кавычку после скобки).
    (re.compile(r'(«О\s+несостоятельности\s*\(банкротстве\))(?!\s*»)'),     r'\1»'),
    # А53: «ООО кУО квладимирскаях» / «кУО {Владимирскаях» / «<УО «Владимирскаях» →
    # «ООО «УО «Владимирская»»» (к/</{ = «, х = »). Якорь «[к<]УО + Владимирска».
    (re.compile(r'[кК<]\s*УО\s+[кК<{«]?\s*[Вв]ладимирска[ях]+'),            '«УО «Владимирская»»'),
    # Вариант «ООО «УО» <Владимирскаях» (УО уже в кавычках, далее <Владимирска…).
    # Открывающая ТОЛЬКО <{к (НЕ «), иначе правило повторно матчит уже-исправленную
    # форму «УО «Владимирская»» и плодит лишние »».
    (re.compile(r'«\s*УО\s*»?\s*[<к{]\s*[Вв]ладимирска[ях]+'),              '«УО «Владимирская»»'),
    # А53 кредитор: «<THС энерго Ростов-надонух» → ««ТНС энерго Ростов-на-Дону»».
    # OCR даёт ЛАТИНИЦУ: T→Т, H→Н, C→С; <=«, х=». »? — орг-правило могло сделать
    # «ТНС». Ставим ДО общего «Ростов-на-Дону». Конкретный кредитор — безопасно.
    (re.compile(r'[<«]?\s*[ТT][НH][СC]\s*»?\s+энерго\s+Ростов[-\s]*на[-\s]*[дД]ону[хж»]?'),
                                                                            '«ТНС энерго Ростов-на-Дону»'),
    # «Владимирскаях» (хвостовая х = ») → «Владимирская»» в формах с двумя верными «.
    (re.compile(r'[Вв]ладимирска[ях]*х(?![а-яё])'),                         'Владимирская»'),
    # Предохранитель: 3+ закрывающих кавычки подряд (двойной матч правил) → «»».
    (re.compile(r'»{3,}'),                                                  '»»'),
    # Общий «Ростов-надону»/«Ростов на дону» → «Ростов-на-Дону».
    (re.compile(r'Ростов[-\s]*на[-\s]*[дД]ону\b'),                          'Ростов-на-Дону'),
    # Инициалы судьи: «Панова К: 0.» → «Панова К.О.» (двоеточие→точка, 0→О).
    (re.compile(r'\b([А-ЯЁ])\s*:\s*0\.'),                                   r'\1.О.'),
    # Инициалы в подписи: «Зайнуллина Д Р:» → «Зайнуллина Д.Р.» (OCR теряет точки).
    (re.compile(r'\b([А-ЯЁ][а-яё]{2,})\s+([А-ЯЁ])\s+([А-ЯЁ])[:.]?(?![а-яё])'),
                                                                            r'\1 \2.\3.'),

    # ── Символьные правила (раунд после прогона с LLM) ───────────────────────
    # «r.» (латинская r) перед городом → «г.»  («r. Ростов-на-Дону» → «г. Ростов…»)
    (re.compile(r'\br\.\s+(?=[А-ЯЁ])'),                                     'г. '),
    # Хвостовые гомоглифы закрывающей » (э/ж/х/ю) у конкретных слов/названий.
    (re.compile(r'\bорганизациях[эжю]'),                                    'организациях»'),
    (re.compile(r'\bэлектроэнергетике[жюэх>]'),                             'электроэнергетике»'),
    # «Айди Коллектэ» → «Айди Коллект» (орг-правило кавычек выше уже поставило »,
    # остаётся убрать «э» = ошибочно прочитанную »; форма имени конкретна — безопасно).
    (re.compile(r'\bКоллектэ\b'),                                           'Коллект'),
    # Мусорный одиночный токен OCR (помарка/штамп): «wlw» / «'wlw» → удалить.
    (re.compile(r"(?<!\w)[«'ʼ‘]?wlw(?!\w)"),                                ''),
    # Битый блок сумм: «2ООО»→«2000» (кир. О=0), «долгапо»→«долга по», «шроцентам»→«процентам».
    (re.compile(r'\b2ООО\b'),                                               '2000'),
    (re.compile(r'\bдолгапо\b', re.IGNORECASE),                            'долга по'),
    (re.compile(r'\bшроцентам\b', re.IGNORECASE),                          'процентам'),
    # «руб руб руб …» (3+ подряд) — колоночный артефакт → одно «руб.».
    (re.compile(r'(?:\bруб\b[.,]?\s*){3,}'),                                'руб. '),
    # URL ЕГРЮЛ: «legrul лnalog ru» → «egrul.nalog.ru» (l→e, лn→n, пробелы→точки).
    (re.compile(r'\blegrul\s+лnalog\s+ru\b', re.IGNORECASE),               'egrul.nalog.ru'),

    # ── Штамп электронной подписи (ЭП) ───────────────────────────────────────
    # «выдана <ФИО> Кому» → «Кому выдана: <ФИО>» (OCR ставит «Кому» в конец строки)
    (re.compile(r'(?i)^выдана\s+(.+?)\s+Кому\s*$'),                          r'Кому выдана: \1'),
    # «Удостовсряюший/Удостоверяюший центр» → «Удостоверяющий центр»
    (re.compile(r'\bУдостов[ес]ряю[шщ]и[йи]?\b', re.IGNORECASE),             'Удостоверяющий'),
    # Время в штампе: «30.05.2025 10.36.12» → «30.05.2025 10:36:12»
    (re.compile(r'(\d{2}\.\d{2}\.\d{4}\s+\d{1,2})\.(\d{2})\.(\d{2})\b'),     r'\1:\2:\3'),

    # ── Mixed-case OCR-артефакты (случайный капслок внутри слова) ────────────
    # EasyOCR / Docling иногда капслочит отдельные буквы в середине слова.
    # Исправляем конкретные частые случаи в юридических документах.
    (re.compile(r'\bсвОИ\b'),                                               'свои'),
    (re.compile(r'\bВКЛЮЧИТЬ\s+В\b'),                                      'включить в'),
    (re.compile(r'\bВКЛЮЧИТЬ\b'),                                           'включить'),
    (re.compile(r'\bДОЛЖНЫ\b'),                                             'должны'),
    (re.compile(r'\bПОЛНОМ\b'),                                             'полном'),
    # «вноситьсЯ» / «предъявляЕТ» — заглавная в СЕРЕДИНЕ слова (после строчной, перед строчной)
    (re.compile(r'([а-яё])([А-ЯЁ])(?=[а-яё])'),                            lambda m: m.group(1) + m.group(2).lower()),
    # «словоЯ» / «объемЕ» — заглавная в КОНЦЕ слова (после 2+ строчных, перед не-буквой)
    (re.compile(r'([а-яё]{2,})([А-ЯЁ])(?=[\s.,;:!?»)\-]|$)'),             lambda m: m.group(1) + m.group(2).lower()),
    # «ПО кредиту» в начале или после пробела — если следует строчная, ПО → по
    (re.compile(r'(?<=\s)ПО(?=\s+[а-яё])'),                                'по'),
    # «вноситься В соответствии» — одиночная «В» между строчными словами = предлог «в»
    (re.compile(r'(?<=[а-яё])\s+В\s+(?=[а-яё])'),                          ' в '),
    # «заем И уплатить» — одиночная «И» между строчными словами = союз «и»
    (re.compile(r'(?<=[а-яё])\s+И\s+(?=[а-яё])'),                          ' и '),
    # «коп ;» / «коп;» в конце суммы → «коп.»
    (re.compile(r'(\d+\s+коп)\s*;'),                                        r'\1.'),
    # «проценты: Срок банку» / «проценты: Срок. банку» — OCR-артефакт.
    # В кредитных договорах: «уплачивать проценты банку», а «Срок» из другой строки
    # попадает между «проценты» и «банку» при некорректном порядке чтения OCR.
    (re.compile(r'(проценты)\s*[:.]?\s+[А-ЯЁ]\S*\s+(банку)', re.IGNORECASE), r'\1 \2'),
    # «д.2 обл ,» — «обл .,» стоит в конце адреса вместо позиции после «Ростовская».
    # Удаляем стоящий в конце «обл» (с возможными пробелами/запятыми/точками).
    (re.compile(r'\s+обл\s*[,.]?\s*$', re.IGNORECASE), ''),
    # «Ростовская Мясниковский» → «Ростовская обл., Мясниковский» (пропущено «обл.»)
    (re.compile(r'Ростовская\s+(?=Мясниковский|[А-ЯЁ]\w+ский\s+р)', re.IGNORECASE),
     'Ростовская обл., '),
    # «гор:» → «гор.» (двоеточие вместо точки после «гор»)
    (re.compile(r'\bгор\s*:\s*(?=[А-ЯЁ])'), 'гор. '),
    # «обл:» / «обл :» → «обл.» в середине текста
    (re.compile(r'\bобл\s*:\s*(?=\s|,|$)'), 'обл. '),
    # «обл. ,» / «обл ,» с пробелом перед запятой → «обл.,»
    (re.compile(r'\bобл\.?\s+,'), 'обл.,'),

    # ── Латиница после дат и в денежных сокращениях ──────────────────────────
    # «28.10.2024 r.» / «2024 r.,» — латинская r после года → «г.»
    (re.compile(r'(\d{4})\s*r\b\.?'), r'\1 г.'),
    # «py6» → «руб» (латиница вместо кириллицы в сумме)
    (re.compile(r'\bpy6\b'), 'руб'),
    # «25 kon.» / «25 коn.» → «25 коп.»
    (re.compile(r'\b[kк][оo]n\b'), 'коп'),
    # «25 кон.» после цифры — OCR путает п/н в «коп.»
    (re.compile(r'(?<=\d )кон\b'), 'коп'),
]

# ── Капс-аббревиатуры: латиница-двойник → кириллица ─────────────────────────
# OCR читает кириллические аббревиатуры латиницей: «KHII» вместо «КНП»,
# «COB» вместо «СОВ». Двойники однозначны в верхнем регистре.
_CAPS_LOOKALIKE = str.maketrans("ABCEHKMOPTX", "АВСЕНКМОРТХ")


def _fix_caps_token(m: re.Match) -> str:
    tok = m.group(0)
    conv = tok.replace("II", "П").translate(_CAPS_LOOKALIKE)
    # Конвертируем только если результат стал ПОЛНОСТЬЮ кириллическим:
    # частичная конверсия («BMW» → «ВМW») хуже оригинала.
    if re.fullmatch(r'[А-ЯЁ]{2,}', conv):
        return conv
    return tok


# Смешанный токен (есть и кириллица, и латиница): «КНII», «ЗАЯBЛЕНИЕ»
_MIXED_CAPS_RE = re.compile(
    r'\b(?=[А-ЯЁA-Z]*[А-ЯЁ])(?=[А-ЯЁA-Z]*[A-Z])[А-ЯЁA-Z]{2,10}\b')
# Чисто латинский КОРОТКИЙ токен после кириллического слова: «решение KHII о…».
# Длина <= 4: марки («TOYOTA», «NISSAN») длиннее либо содержат неконвертируемые
# буквы и остаются как есть.
_LATIN_CAPS_RE = re.compile(r'(?<=[а-яё] )[A-ZI]{2,4}(?=[\s.,;:)!?»]|$)')


def fix_quotes(text: str) -> str:
    for pattern, repl in _QUOTE_PATTERNS:
        text = pattern.sub(repl, text)
    return text


_CENTRINVEST_PHONE = "тел./факс: (863) 2-000-000, www.centrinvest.ru, "
_RE_CENTRINVEST_MISSING_PHONE = re.compile(
    r'(?<!\S)(344\s*000[,.]?\s*)(?=welcome@centrinvest)',
    re.IGNORECASE,
)


def _restore_centrinvest_phone(text: str) -> str:
    """Восстанавливает строку тел./факс если OCR её пропустил.

    EasyOCR стабильно пропускает строку «тел./факс: (863) 2-000-000,
    www.centrinvest.ru,» в шапке ПАО КБ «Центр-инвест». Детектируем по
    паттерну «344000, welcome@» и вставляем известные реквизиты.
    """
    return _RE_CENTRINVEST_MISSING_PHONE.sub(
        lambda m: m.group(1) + _CENTRINVEST_PHONE,
        text,
    )


def postprocess(text: str) -> str:
    """Применяет все OCR-исправления символов, затем нормализует кавычки."""
    for pattern, repl in _OCR_CHAR_FIXES:
        text = pattern.sub(repl, text)
    # Капс-аббревиатуры: латиница-двойник → кириллица («KHII» → «КНП»)
    text = _MIXED_CAPS_RE.sub(_fix_caps_token, text)
    text = _LATIN_CAPS_RE.sub(_fix_caps_token, text)
    text = _restore_centrinvest_phone(text)
    return fix_quotes(text)


# ── Инлайн-чистка тела: непарные скобки/кавычки + одиночные шум-токены ─────────
_INLINE_NOISE_CHARS = set("|{}\\^~°*")         # символы, не встречающиеся в тексте
_QUOTE_CHARS = "'\"«»‚’`“”"
_GLUED_CLOSE_QUOTE_RE = re.compile(r'»(?=[А-Яа-яЁёA-Za-z])')  # »Санколлект → Санколлект
_LETTERS_RE = re.compile(r'[^A-Za-zА-Яа-яЁё]')
# Валидные однобуквенные русские слова: одиночная такая буква — не мусор.
_CYR_1LETTER_OK = set("аиоувкся")


def _remove_unmatched_pairs(text: str, op: str, cl: str) -> str:
    """Удаляет НЕПАРНЫЕ op/cl (стек): орфанный «)» без «(» и наоборот."""
    stack: list[int] = []
    remove: set[int] = set()
    for i, c in enumerate(text):
        if c == op:
            stack.append(i)
        elif c == cl:
            if stack:
                stack.pop()
            else:
                remove.add(i)
    remove.update(stack)
    if not remove:
        return text
    return "".join(c for i, c in enumerate(text) if i not in remove)


def _homoglyph_fix_token(tok: str) -> str:
    """Если буквенное ядро токена — латиница-гомоглиф известного рус. слова
    («Ha»→«На», «He»→«Не»), заменить на кириллицу. Иначе токен без изменений.
    Спасает слова-мисриды от удаления правилом «короткая латиница-мусор»."""
    core = tok.strip(_QUOTE_CHARS)
    letters = _LETTERS_RE.sub("", core)
    if letters and letters.isascii() and len(letters) <= 4:
        cyr = _lat_homoglyph_word(letters)
        if cyr:
            return tok.replace(letters, cyr, 1)
    return tok


def _is_noise_token(tok: str) -> bool:
    """True если токен — инлайн-мусор. Мусор: только кавычки/апострофы (непарные),
    только шум-символы (| } \\ ° * ^ ~), короткая латиница ≤3 букв («ero», «i», «f»
    — среди русского текста это OCR-шум; 4+ букв как «GmbH»/«NISSAN» сохраняем),
    одиночная НЕсловарная кириллица («Й», «ц»; предлоги «в»/«с» сохраняем).
    Числа, пунктуация-слова, парные знаки — НЕ мусор."""
    t = tok.strip()
    if not t:
        return False
    core = t.strip(_QUOTE_CHARS)
    if not core:                                    # только кавычки/апострофы
        return True
    if any(c.isdigit() for c in core):              # есть цифра — число/реквизит
        return False
    if all(c in _INLINE_NOISE_CHARS for c in core):  # только шум-символы
        return True
    letters = _LETTERS_RE.sub("", core)
    if letters:
        # Короткая СТРОЧНАЯ латиница — OCR-шум («ero», «ees», «rs», «i», «f»).
        # Заглавную/смешанную (коды моделей «JF», «IL», «GmbH») НЕ трогаем.
        if letters.isascii() and len(letters) <= 3 and letters.islower():
            return True
        # ГОЛАЯ одиночная кириллица («ч», «Й») — мусор; «п.»/«л.»/«г.» (буква+точка)
        # — сокращения, НЕ трогаем (len(core)==1 отсекает их).
        if len(core) == 1 and not letters.isascii():
            return core.lower() not in _CYR_1LETTER_OK
    return False


def clean_body_text(text: str) -> str:
    """Снимает инлайн-OCR-мусор из абзаца тела/заголовка (в таблицах НЕ вызывается):
    закрывающую кавычку, приклеенную к слову («»Санколлект»→«Санколлект»), непарные
    « » ( ) [ ], и одиночные шум-токены (лат. буква, |}\\°*^~, одинокие кавычки).
    Числа, слова, парные знаки, предлоги — сохраняются."""
    if not text:
        return text
    text = _GLUED_CLOSE_QUOTE_RE.sub("", text)
    # Балансируем только скобки. Кавычки «» НЕ балансируем: длинная цитата
    # («Об утверждении…») часто разорвана между блоками — тогда открывающая «
    # без закрытия ЛЕГИТИМНА. Стрелые закрывающие »-к-слову ловит glued-регекс выше.
    for op, cl in (("(", ")"), ("[", "]")):
        text = _remove_unmatched_pairs(text, op, cl)
    kept: list[str] = []
    for t in text.split(" "):
        t = _homoglyph_fix_token(t)     # «Ha»→«На» ДО проверки на латиницу-мусор
        if _is_noise_token(t):
            continue
        t = t.strip("'\"`‚’")           # снять приклеенные ПРЯМЫЕ кавычки/апострофы («» не трогаем)
        if t:
            kept.append(t)
    return re.sub(r"\s{2,}", " ", " ".join(kept)).strip()
